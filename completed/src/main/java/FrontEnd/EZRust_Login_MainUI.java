    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //user name can't change//
        String username = "Rust";
        String uipassword = "";

        String inputpassword = txtpassword.getText();//password input//
        
        try {
            Connection c1 = DriverManager.getConnection(url, user, password);

            String sql = "select password from rustlogin";

            Statement s2 = c1.createStatement();
            ResultSet r2 = s2.executeQuery(sql);

            while (r2.next()) {
                uipassword = r2.getString("password");
            }

            if (username.equalsIgnoreCase(txtname.getText()) && uipassword.equals(inputpassword)) {
                JOptionPane.showMessageDialog(rootPane, "Welcome :)");
                V2.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "wrong password or username try again");
            }
            c1.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Sorry . There was an error to connect SQl sever :(");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

